extern s32 D_801CDCAC;
extern s32 D_801CDD30;
extern s32* D_801CDDC4;
extern volatile s32* D_801CDDD0;
extern s32* D_801CDDF0;

void func_801C1F34(s32 arg0) {
    switch (arg0) {
    case 0:
        *D_801CDDF0 = 0x80000000;
        *D_801CDDC4 = 0;
        *D_801CDDD0 = 0;
        *D_801CDDF0 = 0x60000000;
        func_801C2024(&D_801CDCAC, 0x20);
        func_801C2024(&D_801CDD30, 0x20);
        return;
    case 1:
        *D_801CDDF0 = 0x80000000;
        *D_801CDDC4 = 0;
        *D_801CDDD0 = 0;
        *D_801CDDD0;
        *D_801CDDF0 = 0x60000000;
        return;
    default:
        printf("MDEC_rest:bad option(%d)\n", arg0);
        return;
    }
}

extern s32* D_801CDDBC;
extern s32* D_801CDDC0;
extern s32* D_801CDDC4;
extern s32* D_801CDDC8;
extern s32* D_801CDDCC;
extern s32* D_801CDDD0;
extern s32* D_801CDDEC;
extern volatile s32* D_801CDDF0;
extern s32* D_801CDDF4;

void func_801C2024(s32* arg0, u32 arg1) {
    MDEC_in_sync();
    *D_801CDDF4 |= 0x88;
    *D_801CDDBC = arg0 + 1;
    *D_801CDDC0 = ((arg1 >> 5) << 0x10) | 0x20;
    *D_801CDDEC = *arg0;
    *D_801CDDC4 = 0x01000201;
}

void func_801C20B4(s32 arg0, u32 arg1) {
    func_801C21D4();
    *D_801CDDF4 |= 0x88;
    *D_801CDDD0 = 0;
    *D_801CDDC8 = arg0;
    *D_801CDDCC = ((arg1 >> 5) << 0x10) | 0x20;
    *D_801CDDD0 = 0x01000200;
}


s32 MDEC_in_sync(void) {
    volatile s32 sp10 = 0x100000;
    while (*D_801CDDF0 & 0x20000000) {
        if (--sp10 == -1) {
            MDEC_print_error("MDEC_in_sync");
            return -1;
        }
    }
    return 0;
}

s32 func_801C21D4(void) {
    volatile s32 sp10 = 0x100000;
    while (*D_801CDDD0 & 0x01000000) {
        if (--sp10 == -1) {
            MDEC_print_error("MDEC_out_sync");
            return -1;
        }
    }
    return 0;
}

