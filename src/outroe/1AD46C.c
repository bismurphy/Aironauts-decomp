void DecDCTin(s32* arg0, s32 arg1) {
    if (arg1 & 1) {
        *arg0 &= 0xF7FFFFFF;
    } else {
        *arg0 |= 0x08000000;
    }
    if (arg1 & 2) {
        *arg0 |= 0x02000000;
    } else {
        *arg0 &= 0xFDFFFFFF;
    }
    func_801C2024(arg0, *(u16*)arg0);
}

void DecDCTout(void) {
    func_801C20B4();
}

